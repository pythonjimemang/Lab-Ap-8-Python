

suhuDerajat = float(input("Masukkan suhu dalm celcius : "))

Kelvin = suhuDerajat + 273
Reamur = suhuDerajat * 4 // 5
Farenheit = (suhuDerajat * 9//5) + 32

print(f"Hasil konversi dari suhu {suhuDerajat} derajat ke kelvin adalah : {Kelvin}K \nHasil konversi dari suhu {suhuDerajat} deerajat ke reamur adalah : {Reamur}R \nHasil konversi dari suhu {suhuDerajat} derajat ke farenheit adalah : {Farenheit}F ")